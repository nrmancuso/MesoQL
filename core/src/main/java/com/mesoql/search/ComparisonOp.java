package com.mesoql.search;

/**
 * Enumeration of comparison operators supported in {@link ComparisonFilterInput}.
 */
public enum ComparisonOp {
    /**
     * Equal.
     */
    EQ,
    /**
     * Not equal.
     */
    NEQ,
    /**
     * Greater than.
     */
    GT,
    /**
     * Greater than or equal.
     */
    GTE,
    /**
     * Less than.
     */
    LT,
    /**
     * Less than or equal.
     */
    LTE
}
